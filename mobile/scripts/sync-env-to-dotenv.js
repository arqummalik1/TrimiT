#!/usr/bin/env node
/**
 * EAS prebuild hook: write EXPO_PUBLIC_* from process.env → .env in the build workspace.
 * Local builds: vars come from shell (load-env-for-build.sh).
 * Cloud builds: vars come from expo.dev Environment variables.
 */
const fs = require('fs');
const path = require('path');

const PREFIXES = ['EXPO_PUBLIC_', 'SENTRY_'];
const STATIC_KEYS = ['SENTRY_DISABLE_AUTO_UPLOAD'];

function collectEnv() {
  const out = {};
  for (const [key, value] of Object.entries(process.env)) {
    if (value == null || value === '') continue;
    if (value.startsWith('$')) continue;
    if (PREFIXES.some((p) => key.startsWith(p)) || STATIC_KEYS.includes(key)) {
      out[key] = value;
    }
  }
  return out;
}

function main() {
  const vars = collectEnv();
  const keys = Object.keys(vars);
  if (keys.length === 0) {
    if (process.env.EAS_BUILD) {
      console.warn('[sync-env-to-dotenv] No EXPO_PUBLIC_* in process.env — check Expo dashboard or mobile/.env');
      process.exit(1);
    }
    console.log('[sync-env-to-dotenv] Skipped (no EAS env vars in process.env).');
    return;
  }

  const envPath = path.join(process.cwd(), '.env');
  const body =
    '# Auto-generated by scripts/sync-env-to-dotenv.js (EAS prebuild)\n' +
    keys.map((k) => `${k}=${vars[k]}`).join('\n') +
    '\n';

  fs.writeFileSync(envPath, body, 'utf8');
  console.log(`[sync-env-to-dotenv] Wrote ${keys.length} keys to ${envPath}`);
}

main();
